#arithmatic
a<-7.5
b<-2
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b)#remainder
print(a%/%b)#quotient
print(a^b)
#for vectors
c1<-c(8,9,6)
c2<-c(2,4,5)
print(c1+c2)
print(c1-c2)
print(c1*c2)
print(c1/c2)
print(c1%%c2)#remainder
print(c1%/%c2)#quotient
print(c1^c2)

#relational operators
a<-7.5
b<-2
print(a<b)
print(a>b)
print(a==b)
print(a<=b)
print(a>=b)#remainder
print(a!=b)#quotient
#for vectors
c1<-c(8,9,6)
c2<-c(2,4,5)
print(c1<c2)
print(c1>c2)
print(c1==c2)
print(c1<=c2)
print(c1>=c2)#remainder
print(c1!=c2)#quotient

#logical
#in vector
d<-c(3.5,TRUE,2+5i)
e<-c(2.4,TRUE,6+5i)
print(d&e)

d<-c(3.5,TRUE,2+5i)
e<-c(2.4,TRUE,6+5i)
print(d&&e)

d<-c(3.5,TRUE,2+5i)
e<-c(2.4,TRUE,6+5i)
print(d|e)

d<-c(3.5,TRUE,2+5i)
e<-c(2.4,TRUE,6+5i)
print(d||e)

d<-c(3.5,TRUE,2+5i)
e<-c(2.4,TRUE,6+5i)
print(!d)

